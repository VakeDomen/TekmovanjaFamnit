public class Planet {
    String name;
    int x;
    int y;
    float planetSize;
    int feetSize;
    String planetColor;

    public Planet(String name, int x, int y, float planetSize, int feetSize, String planetColor) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.planetSize = planetSize;
        this.feetSize = feetSize;
        this.planetColor = planetColor;
    }
}
